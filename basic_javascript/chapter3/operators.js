// 사칙연산과 제곱, 나머지값
let age = 10;

console.log(age + 5);
console.log(age - 5);
console.log(age * 5);
console.log(age / 5);

//10*10*10 10의 3승
console.log(age ** 3);
//3을 나눈 나머지 값.
console.log(age % 3);

//증감 연산자
age++;
console.log(age);
age--;
console.log(age);

//복합대입 연산자
age += 30;
console.log(age);
age -= 5;
console.log(age);
